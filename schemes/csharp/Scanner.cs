    private int Char2State(int chr)
    {
	int m, b = 0, e = c2sArr.Length;
	while (b < e) {
	    m = (b + e) / 2;
	    if (chr < c2sArr[m].keyFrom) e = m;
	    else if (chr > c2sArr[m].keyTo) b = m + 1;
	    else return c2sArr[m].val;
	}
	return 0;
    }

