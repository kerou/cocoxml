    private int Identifier2KWKind(string key, int defaultVal)
    {
	int rc, m, b = 0, e = i2kArr.Length;

	if (!caseSensitive) key = key.ToLower();
	while (b < e) {
	    m = (b + e) / 2;
	    rc = String.Compare(key, i2kArr[m].key);
	    if (rc < 0) e = m;
	    else if (rc > 0) b = m + 1;
	    else return i2kArr[m].val;
	}
	return defaultVal;
    }

    private int GetKWKind(long start, long end, int defaultVal)
    {
	return Identifier2KWKind(buffer.GetString(start, (int)(end - start)),
				 defaultVal);
    }

    private void GetCh()
    {
	if (oldEols > 0) {
	    ch = '\n'; --oldEols; oldEolsEOL= true;
	} else {
	    if (ch == '\n') {
		if (oldEolsEOL) oldEolsEOL = false;
		else {
		    ++line; col = 0;
		}
	    } else if (ch == '\t') {
		col += 8 - col % 8;
	    } else {
		/* FIX ME: May be the width of some specical character
		 * is NOT self->chBytes. */
		col += chBytes;
	    }
	    ch = buffer.Read(out chBytes);
	    pos = buffer.GetPos();
	}
    }

    private class SLock_t {
	public int ch, chBytes;
	public long pos;
	public int line, col;
    };

    private SLock_t LockCh()
    {
	SLock_t slock = new SLock_t();
	slock.ch = ch;
	slock.chBytes = chBytes;
	slock.pos = pos;
	slock.line = line;
	slock.col = col;
	buffer.Lock();
	return slock;
    }

    private void UnlockCh(SLock_t slock)
    {
	buffer.Unlock();
    }

    private void ResetCh(SLock_t slock)
    {
	ch = slock.ch;
	chBytes = slock.chBytes;
	pos = slock.pos;
	line = slock.line;
	buffer.LockReset();
    }

    private class CcsComment_t {
	public int[] start;
	public int[] end;
	public bool nested;

	public CcsComment_t(int start0, int start1,
			    int end0, int end1, bool nested)
	{
	    start = new int[2] { start0, start1 };
	    end = new int[2] { end0, end1 };
	    this.nested = nested;
	}
    }
    static readonly CcsComment_t[] comments = {
	/*---- comments ----*/
	new CcsComment_t('/', '/', '\n', 0, false),
	new CcsComment_t('/', '*', '*', '/', true),
	/*---- enable ----*/
    };

    private bool Comment(CcsComment_t c)
    {
	SLock_t slock = null;
	int level = 1, line0 = line;

	if (c.start[1] != 0) {
	    slock = LockCh(); GetCh();
	    if (ch != c.start[1]) {
		ResetCh(slock);
		return false;
	    }
	    UnlockCh(slock);
	}
	GetCh();
	for (;;) {
	    if (ch == c.end[0]) {
		if (c.end[1] == 0) {
		    if (--level == 0) break;
		} else {
		    slock = LockCh(); GetCh();
		    if (ch == c.end[1]) {
			UnlockCh(slock);
			if (--level == 0) break;
		    } else {
			ResetCh(slock);
		    }
		}
	    } else if (c.nested && ch == c.start[0]) {
		if (c.start[1] == 0) {
		    ++level;
		} else {
		    slock = LockCh(); GetCh();
		    if (ch == c.start[1]) {
			UnlockCh(slock);
			++level;
		    } else {
			ResetCh(slock);
		    }
		}
	    } else if (ch == CcsBuffer_t.EoF) {
		return true;
	    }
	    GetCh();
	}
	oldEols = line - line0;
	GetCh();
	return true;
    }

    public CcsToken_t NextToken()
    {
	long pos;
	int line, col, state, kind; CcsToken_t t;
	int curComment;
	for (;;) {
	    while (ch == ' '
		   /*---- scan1 ----*/
		   || (ch >= '\t' && ch <= '\n')
		   || ch == '\r'
		   /*---- enable ----*/
		   ) GetCh();
	    for (curComment = 0; curComment < comments.Length; ++curComment)
		if (ch == comments[curComment].start[0] &&
		    Comment(comments[curComment])) break;
	    if (curComment >= comments.Length) break;
	}
	pos = this.pos; line = this.line; col = this.col;
	buffer.Lock();
	state = Char2State(ch);
	GetCh();
	kind = noSym;
	switch (state) {
	case -1: kind = eofSym; break;
	case 0: kind = noSym; break;
	    /*---- scan3 ----*/
	case 1: case_1:
	    if ((ch >= '0' && ch <= '9') ||
		(ch >= 'A' && ch <= 'Z') ||
		ch == '_' ||
		(ch >= 'a' && ch <= 'z')) {
		GetCh(); goto case_1;
	    } else { kind = GetKWKind(pos, this.pos, 1); break; }
	case 2: case_2:
	    if ((ch >= '0' && ch <= '9')) {
		GetCh(); goto case_2;
	    } else { kind = 2; break; }
	case 3: case_3:
	    { kind = 3; break; }
	case 4: case_4:
	    { kind = 4; break; }
	case 5:
	    if ((ch >= 0 && ch <= '\t') ||
		(ch >= '\v' && ch <= '\f') ||
		(ch >= 14 && ch <= '&') ||
		(ch >= '(' && ch <= '[') ||
		(ch >= ']' && ch <= 65535)) {
		GetCh(); goto case_6;
	    } else if (ch == '\\') {
		GetCh(); goto case_7;
	    } else { kind = noSym; break; }
	case 6: case_6:
	    if (ch == '\'') {
		GetCh(); goto case_9;
	    } else { kind = noSym; break; }
	case 7: case_7:
	    if ((ch >= ' ' && ch <= '~')) {
		GetCh(); goto case_8;
	    } else { kind = noSym; break; }
	case 8: case_8:
	    if ((ch >= '0' && ch <= '9') ||
		(ch >= 'a' && ch <= 'f')) {
		GetCh(); goto case_8;
	} else if (ch == '\'') {
		GetCh(); goto case_9;
	    } else { kind = noSym; break; }
	case 9: case_9:
	    { kind = 5; break; }
	case 10: case_10:
	    if ((ch >= '0' && ch <= '9') ||
		(ch >= 'A' && ch <= 'Z') ||
		ch == '_' ||
		(ch >= 'a' && ch <= 'z')) {
		GetCh(); goto case_10;
	    } else { kind = 48; break; }
	case 11: case_11:
	    if ((ch >= 0 && ch <= '\t') ||
		(ch >= '\v' && ch <= '\f') ||
		(ch >= 14 && ch <= '!') ||
		(ch >= '#' && ch <= '[') ||
		(ch >= ']' && ch <= 65535)) {
		GetCh(); goto case_11;
	    } else if (ch == '"') {
		GetCh(); goto case_3;
	    } else if (ch == '\\') {
		GetCh(); goto case_12;
	    } else if (ch == '\n' ||
		       ch == '\r') {
		GetCh(); goto case_4;
	    } else { kind = noSym; break; }
	case 12: case_12:
	    if ((ch >= ' ' && ch <= '~')) {
		GetCh(); goto case_11;
	    } else { kind = noSym; break; }
	case 13:
	    { kind = 20; break; }
	case 14:
	    { kind = 26; break; }
	case 15:
	    { kind = 27; break; }
	case 16: case_16:
	    { kind = 28; break; }
	case 17:
	    { kind = 31; break; }
	case 18: case_18:
	    { kind = 32; break; }
	case 19: case_19:
	    { kind = 33; break; }
	case 20:
	    { kind = 34; break; }
	case 21:
	    { kind = 37; break; }
	case 22:
	    { kind = 38; break; }
	case 23:
	    { kind = 39; break; }
	case 24:
	    { kind = 40; break; }
	case 25:
	    { kind = 41; break; }
	case 26: case_26:
	    { kind = 45; break; }
	case 27: case_27:
	    { kind = 46; break; }
	case 28:
	    if (ch == '.') {
		GetCh(); goto case_16;
	    } else if (ch == '>') {
		GetCh(); goto case_19;
	    } else if (ch == ')') {
		GetCh(); goto case_27;
	    } else { kind = 21; break; }
	case 29:
	    if (ch == '.') {
		GetCh(); goto case_18;
	    } else { kind = 30; break; }
	case 30:
	    if (ch == '.') {
		GetCh(); goto case_26;
	    } else { kind = 36; break; }
	    /*---- enable ----*/
	}
	t = new CcsToken_t(kind, pos, col, line,
			   buffer.GetString(pos, (int)(this.pos - pos)));
	buffer.Unlock();
	return t;
    }
